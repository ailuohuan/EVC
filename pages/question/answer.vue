		<view>可以从以下三个方面排查问题所在： </view>
		<view class="subtitle">1. EVC Wallet 的助记词由 12 个英文单词组成</view>
		<view class="subtitle">2. 请确保单词与单词用空格隔开，不要用回车隔开 </view>
