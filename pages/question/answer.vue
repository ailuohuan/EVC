		<view>3. 要将 Keystore 和密码分开存储, 一旦黑客盗取了 Keystore, 在不知道密码的前提下, 也无法快速破解该钱包。</view>
		<view>4. 不要使用邮箱或者其他线上即时通讯软件传输 Keystore 或者密码, 这样很可能会被黑客监听拦截。</view>
